;; This program starts reading at memory address x5000. It prints a large ASCII-art style string of the
;; extended ASCII value of the null-terminated string starting at x5002. It uses the characters in x5000 and x5001 to
;; print this: x5000 is the background (negative) and x5001 is the foreground character.
;; Register use table:
;; | Register | Usage                      | Description                                                       |
;; |----------+----------------------------+-------------------------------------------------------------------|
;; | R0       | OFFSET, TMP_REG            | Used to compute the offset for R5, and as a temp.                 |
;; | R1       | ADDR_{FONT_FILE}           | Address of FONT_FILE                                              |
;; | R2       | ADDR_{FONT_DATA}           | Address of FONT_DATA                                              |
;; | R3       | ROW_CTR                    | Counter for ROW loop.                                             |
;; | R4       | Current location in FONT_F | Tracks current character address of string.                       |
;; | R5       | M[R4], ROW_DATA            | (a) Address of row to print in FONT_DATA (b) Contents of that row |
;; | R6       | SHIFT_CTR, COL_CTR         | Counter for left shifting R5, counter for COL loop after.         |


        .ORIG   x3000
        LD      R1,FONT_F       ; Store 0x5000 in R1 (address of FONT_FILE)
        LEA     R2,FONT_DATA    ; Store the address of FONT_DATA in R4
        AND     R3,R3,#0        ; Clear R3 for use as R_CTR
        ADD     R3,R3,#15       ; Set R3 to 15, ready for R_CTR

R_LOOP  ADD     R4,R1,#1        ; Set R4 to x5001
S_LOOP  ADD     R4,R4,#1        ; Set R4 to x5002
        LDR     R5,R4,#0        ; Store the character to print in R5
        BRz     BRK             ; If this character is NULL, break.
        ;; Multiply R5 by 4
        AND     R6,R6,#0        ; Clear R6 to prepare for use as SHIFT_CTR
        ADD     R6,R6,#4        ; We want to do 4 shifts, set SHIFT_CTR to 4.
DBL     ADD     R5,R5,R5        ; Left shift R5
        ADD     R6,R6,#-1       ; Decrement SHIFT_CTR
        BRp     DBL             ; Loop to DBL if SHIFT_CTR is positive
        ADD     R5,R5,R2        ; Now that R5 is doubled, add ADDR_{FONT_DATA} to it.
        ;; R5 is now at the starting row of the character we need to print
        ADD     R0,R3,#-15      ; To compute OFFSET, first subtract 15 from the current ROW_CTR
        NOT     R0,R0           ; OFFSET has the wrong sign, two's complement it to flip.
        ADD     R0,R0,#1        ; Add 1 to OFFSET.
        ADD     R5,R5,R0        ; Add the OFFSET to R5
        ;; R5 is now at correct memory location
        LDR     R5,R5,#0        ; Get the ROW_DATA of the memory location R5 is at.
        ;;  R5 is now ROW_DATA
        ADD     R6,R6,#8        ; R6 is zero, so set it to 8 (COL_CTR)
C_LOOP  AND     R0,R5,#-1       ; Get the MSB of R5, store it in TMP_REG R0
        BRn     NEG             ; If the MSB was 1, GOTO NEG
        LDR     R0,R1,#0        ; If the MSB was 0, set R0 to the char at 0x5000
        BR      PRNT            ; GOTO PRNT
NEG     LDR     R0,R1,#1        ; Since MSB is 1, set R0 to the char at 0x5001
PRNT    TRAP    x21             ; Print the char in R0
        ADD     R5,R5,R5        ; Left shift R5
        ADD     R6,R6,#-1       ; Decrement COL_CTR
        BRp     C_LOOP          ; Loop to C_LOOP while COL_CTR is positive
        BR      S_LOOP          ; Loop to S_LOOP
BRK     LD      R0, NEWLINE     ; One row printed, time to print a NEWLINE.
        TRAP    x21             ; Print the NEWLINE.
        ADD     R3,R3,#-1       ; Decrement ROW_CTR
        BRzp    R_LOOP          ; Loop to R_LOOP while ROW_CTR is not negative.
        TRAP    x25             ; HALT
FONT_F  .FILL   x5000
NEWLINE .FILL   x0A

